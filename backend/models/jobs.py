class Jobs:
    def __init__(self, title, description, years_of_exp, location, job_type, visa_sponsorship, skills, salary):
        self.title = title
        self.description = description
        self.years_of_exp = years_of_exp
        self.location = location
        self.job_type = job_type
        self.visa_sponsorship = visa_sponsorship
        self.skills = skills
        self.salary = salary